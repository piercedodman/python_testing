import sys

# use %len(sys.argv[1].strip()) for printing the chars in the bitmap

def main():
    map = """
....................................................................
   **************   *  *** **  *      ******************************
  ********************* ** ** *  * ****************************** *
 **      *****************       ******************************
          *************          **  * **** ** ************** *
           *********            *******   **************** * *
            ********           ***************************  *
   *        * **** ***         *************** ******  ** *
               ****  *         ***************   *** ***  *
                 ******         *************    **   **  *
                 ********        *************    *  ** ***
                   ********         ********          * *** ****
                   *********         ******  *        **** ** * **
                   *********         ****** * *           *** *   *
                     ******          ***** **             *****   *
                     *****            **** *            ********
                    *****             ****              *********
                    ****              **                 *******   *
                    ***                                       *    *
                    **     *                    *
....................................................................
"""
    text = list(sys.argv[1])
    #print(text)
    nmap = []
    for m in range(len(map)):
        if map[m].isspace():
            nmap.append(map[m])
        if map[m] == "\n":
            nmap.append("\n")
        else:
            nmap.append(text[m %len(text)])
    print(''.join(nmap))

if __name__ == "__main__":
    main()